# Check the Enter number
user_input = int(input("Enter your number:"))
if user_input > 0:
    print(f"Your entered number is positive: {user_input}")
if user_input < 0:
    print(f"Your entered number is negtive: {user_input}")
if user_input == 0:
    print(f"Your entered number is Equal to Zero: {user_input}")